# Exercício 3.3 - Complete a tabela a seguir utilizando a = True, b = False e c = True.
                        # Expressão Resultado
                        # a and a   =   True or False
                        # b and b   =   True or False
                        # not c     =   True or False
                        # not b     =   True or False
                        # not a     =   True or False
                        # a and b   =   True or False
                        # b and c   =   True or False
                        # a or c    =   True or False
                        # b or c    =   True or False
                        # c or a    =   True or False
                        # c or b    =   True or False
                        # c or c    =   True or False
                        # b or b    =   True or False
                        
a = True
b = False
c = True

print('\n')

print('a and a = \t{}'.format(a and a))     # True
print('b and b = \t{}'.format(b and b))     # False
print('not c = \t{}'.format(not c))         # False
print('not b = \t{}'.format(not b))         # True
print('not a = \t{}'.format(not a))         # False
print('a and b = \t{}'.format(a and b))     # False
print('b and c = \t{}'.format(b and c))     # False
print('a or c = \t{}'.format(a or c))       # True
print('b or c = \t{}'.format(b or c))       # True 
print('c or a = \t{}'.format(c or a))       # True
print('c or b = \t{}'.format(c or b))       # True
print('c or c = \t{}'.format(c or c))       # True 
print('b or b = \t{}'.format(b or b))       # False

print('\n')